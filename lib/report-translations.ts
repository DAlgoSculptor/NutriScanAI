export const translateHealthAlert = (alert: string, languageCode: string) => {
  // Translate health alerts based on language
  const alertTranslations: { [key: string]: { [key: string]: string } } = {
    "has been recently banned in several countries": {
      es: "ha sido recientemente prohibido en varios países",
      fr: "a été récemment interdit dans plusieurs pays",
      de: "wurde kürzlich in mehreren Ländern verboten",
      it: "è stato recentemente vietato in diversi paesi",
      pt: "foi recentemente banido em vários países",
      ru: "недавно был запрещен в нескольких странах",
      ja: "最近いくつかの国で禁止されました",
      ko: "최근 여러 국가에서 금지되었습니다",
      zh: "最近在几个国家被禁止",
      ar: "تم حظره مؤخراً في عدة دول",
      hi: "हाल ही में कई देशों में प्रतिबंधित किया गया है",
    },
  }

  let translatedAlert = alert
  Object.entries(alertTranslations).forEach(([english, translations]) => {
    if (alert.includes(english)) {
      translatedAlert = alert.replace(english, translations[languageCode] || english)
    }
  })

  return translatedAlert
}
